CREATE TABLE IF NOT EXISTS vpn_servers (
    id INT(10) UNSIGNED NOT NULL AUTO_INCREMENT,
    name VARCHAR(255) NOT NULL,
    code VARCHAR(80) NOT NULL,
    country_code CHAR(2) NULL,
    country_name VARCHAR(120) NULL,
    flag_emoji VARCHAR(16) NULL,
    show_flag TINYINT(1) UNSIGNED NOT NULL DEFAULT 1,
    country VARCHAR(120) NULL,
    city VARCHAR(120) NULL,
    panel_url VARCHAR(500) NOT NULL,
    public_host VARCHAR(255) NULL,
    panel_path VARCHAR(190) NULL,
    api_auth_type VARCHAR(30) NOT NULL DEFAULT 'token',
    api_token_encrypted MEDIUMTEXT NULL,
    username_encrypted MEDIUMTEXT NULL,
    password_encrypted MEDIUMTEXT NULL,
    status VARCHAR(30) NOT NULL DEFAULT 'unchecked',
    is_enabled TINYINT(1) UNSIGNED NOT NULL DEFAULT 1,
    last_check_at DATETIME NULL,
    last_success_at DATETIME NULL,
    last_error TEXT NULL,
    sort_order INT(10) UNSIGNED NOT NULL DEFAULT 0,
    created_at DATETIME NOT NULL,
    updated_at DATETIME NOT NULL,
    PRIMARY KEY (id),
    UNIQUE KEY code (code),
    KEY country_code (country_code),
    KEY status (status),
    KEY is_enabled (is_enabled),
    KEY sort_order (sort_order)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS vpn_inbounds (
    id INT(10) UNSIGNED NOT NULL AUTO_INCREMENT,
    server_id INT(10) UNSIGNED NOT NULL,
    remote_inbound_id VARCHAR(80) NULL,
    name VARCHAR(255) NOT NULL,
    protocol VARCHAR(80) NULL,
    remark VARCHAR(255) NULL,
    port INT(10) UNSIGNED NULL,
    is_enabled TINYINT(1) UNSIGNED NOT NULL DEFAULT 1,
    status VARCHAR(40) NOT NULL DEFAULT 'active',
    settings_json MEDIUMTEXT NULL,
    stream_settings_json MEDIUMTEXT NULL,
    created_at DATETIME NOT NULL,
    updated_at DATETIME NOT NULL,
    PRIMARY KEY (id),
    UNIQUE KEY server_remote_inbound (server_id, remote_inbound_id),
    KEY server_id (server_id),
    KEY remote_inbound_id (remote_inbound_id),
    KEY status (status),
    KEY is_enabled (is_enabled)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

