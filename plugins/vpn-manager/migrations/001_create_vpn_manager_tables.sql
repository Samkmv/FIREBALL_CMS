    client_uuid VARCHAR(120) NULL,
    client_email VARCHAR(255) NULL,
    client_remark VARCHAR(255) NULL,
    subscription_item_name VARCHAR(255) NULL,
    status VARCHAR(40) NOT NULL DEFAULT 'creating',
    traffic_limit_bytes BIGINT UNSIGNED NOT NULL DEFAULT 0,
    traffic_used_bytes BIGINT UNSIGNED NOT NULL DEFAULT 0,
    last_sync_at DATETIME NULL,
    last_error TEXT NULL,
    created_at DATETIME NOT NULL,
    updated_at DATETIME NOT NULL,
    PRIMARY KEY (id),
    UNIQUE KEY subscription_server_inbound (subscription_id, server_id, inbound_id),
    KEY subscription_id (subscription_id),
    KEY server_id (server_id),
    KEY inbound_id (inbound_id),
    KEY status (status),
    KEY client_email (client_email),
    CONSTRAINT fk_vpn_nodes_subscription FOREIGN KEY (subscription_id) REFERENCES vpn_subscriptions(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

